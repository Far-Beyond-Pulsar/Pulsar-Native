/// Push filled-quads for each set pixel in the 5×7 glyph bitmap.
/// Returns total pixel-width consumed.
#[inline(always)]
pub fn push_text(
    text: &str,
    x: f32,
    y: f32,
    color: [f32; 4],
    pixel_scale: f32,
    out: &mut Vec<RectInstance>,
) -> f32 {
    let mut cx = x;
    let bytes = text.as_bytes();
    let mut i = 0usize;
    while i < bytes.len() {
        let idx = bytes[i] as usize;
        let rows = &GLYPHS[if idx < 128 { idx } else { 0 }];
        let mut r = 0u32;
        while r < 7 {
            let row_bits = rows[r as usize];
            if row_bits != 0 {
                let mut c = 0u32;
                while c < 5 {
                    if (row_bits >> (4 - c)) & 1 != 0 {
                        out.push(RectInstance {
                            pos: [cx + c as f32 * pixel_scale, y + r as f32 * pixel_scale],
                            size: [pixel_scale, pixel_scale],
                            color,
                            kind: 0,
                            _pad: [0; 3],
                        });
                    }
                    c += 1;
                }
            }
            r += 1;
        }
        cx += 6.0 * pixel_scale;
        i += 1;
    }
    cx - x
}
